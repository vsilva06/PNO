public class Calculadora {

    public double sumar(double num0, double num1){
        return num0 +num1;
    }

    public double restar(double num0, double num1){
        return num0-num1;
    }

    public double multiplicar(double num0, double num1){
        return num0*num1;
    }

    public double dividir(double num0, double num1){
        return num0/num1;
    }

}
